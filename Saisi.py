# Faire la multiplication de deux entiers

# Création des deux variables, faire la multiplication et afficher le résultat.
nombre1 = input("Le premier nombre est : ")
nombre2 = input("Le deuxième nombre est : ")
s = int(nombre1) * int(nombre2)
print("le résultat de {0} et {1} est de : {2} ".format(nombre1, nombre2, s))